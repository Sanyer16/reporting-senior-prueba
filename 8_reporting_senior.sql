/*
R:
OPCIÓN 1: Desde Excel conectarse a SQL Server

-Abre Excel.
-Ve a la pestaña "Datos".
-Clic en "Obtener datos" → "Desde base de datos" → "Desde SQL Server".
-Ingresa el nombre del servidor SQL y la base de datos.
-Elige la tabla o escribe tu propia consulta SQL.
-Importa los datos a tu hoja.

OPCIÓN 2: Exportar desde SQL Server a Excel (por Management Studio)

-Abre SQL Server Management Studio (SSMS).
-Haz clic derecho en la base de datos → "Tasks" → "Export Data...".
-Selecciona como origen tu base de datos.
-Como destino, elige Microsoft Excel.
-Puedes elegir exportar tablas o usar una consulta personalizada.
-Completa el asistente y se exportará.


*/